# single line Comment.

'''
Multi line Comment.
'''
print("Hello World")

# Escape Sequences.
print("Hello, \n    Yash")  # Here, \n is a escape sequence used to get a new line.
print("I am \"Yash Dave\"") # Here, \"  \" is a escape sequence used to print " " in the output.

# More about print function.
print("Hello", 6, 8)      # We can print multiple things in one print function using a comma(,),

# Separator.
print("HEllO", "Yash", sep="<")     #sep=" " will separate all with that symbol.